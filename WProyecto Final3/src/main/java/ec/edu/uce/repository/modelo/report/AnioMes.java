package ec.edu.uce.repository.modelo.report;

public class AnioMes {

	private int anio;
	
	private int mes;

	public AnioMes() {
		
	}
	
	public AnioMes(int anio, int mes) {
		super();
		this.anio = anio;
		this.mes = mes;
	}

	public int getAnio() {
		return anio;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}
	
	
	
}
